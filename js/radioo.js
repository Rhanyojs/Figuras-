// Area del circulo
// Perimetro del circulo

pi = 3.1416;
//Solicitar el radio
radio = float(input("introduce el radio del circculo"));

// area del circulo
// perimetro del circulo
area = pi * radio ** 2;
per = 2 * radio * pi;

//datos mostrados
print("Area = ", area);
print("Perimetro = ", per);

